package cn.tties.maint.holder;

import android.view.View;
import android.widget.LinearLayout;
import android.widget.Spinner;

import cn.tties.maint.R;

public class BaseInfoHolder {

    public Spinner spinnerPowerUtilizationNature;

    public Spinner spinnerVoltageLevel;

    public Spinner spinnerPowerFactor;

    public LinearLayout layoutInfo;

    public BaseInfoHolder(View contentView) {
        this.spinnerPowerUtilizationNature = (Spinner) contentView.findViewById(R.id.spinner_powerUtilizationNature);
        this.spinnerVoltageLevel = (Spinner) contentView.findViewById(R.id.spinner_voltage_level);
        this.spinnerPowerFactor = (Spinner) contentView.findViewById(R.id.spinner_power_factor);
        this.layoutInfo = (LinearLayout) contentView.findViewById(R.id.layout_baseinfo);
    }
}